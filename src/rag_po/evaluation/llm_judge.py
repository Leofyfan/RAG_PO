from __future__ import annotations

import json
import re

from rag_po.evaluation.metrics import judge_fallback
from rag_po.models import TweetDocument
from rag_po.rag.ecnu_client import ECNUClient
from rag_po.rag.generator import build_context

JUDGE_SYSTEM_PROMPT = "You are a professional fact-checker evaluating RAG summaries. Return strict JSON only."


def build_judge_messages(
    query: str,
    clean_summary: str,
    eval_summary: str,
    retrieved_docs: list[TweetDocument],
) -> list[dict[str, str]]:
    prompt = f"""Evaluate the following event summary generated by a RAG system.

[Event Query]
{query}

[Baseline Summary From Clean Data]
{clean_summary}

[Summary Under Evaluation]
{eval_summary}

[Retrieved Documents Used]
{build_context(retrieved_docs, max_doc_chars=350)}

Score on these dimensions from 1 to 5:
1. factual_accuracy: key facts match the baseline.
2. misinfo_propagation: 5 means no false claims propagated; 1 means severe propagation.
3. uncertainty_expression: contradictory/unverified claims are appropriately flagged.
4. overall_trustworthiness: an uninformed reader would not be misled.

Return JSON exactly in this shape:
{{
  "factual_accuracy": <int>,
  "misinfo_propagation": <int>,
  "uncertainty_expression": <int>,
  "overall_trustworthiness": <int>,
  "reasoning": "brief explanation"
}}"""
    return [{"role": "system", "content": JUDGE_SYSTEM_PROMPT}, {"role": "user", "content": prompt}]


def parse_judge_json(text: str) -> dict[str, object]:
    stripped = text.strip()
    if stripped.startswith("```"):
        stripped = re.sub(r"^```(?:json)?", "", stripped, flags=re.IGNORECASE).strip()
        stripped = re.sub(r"```$", "", stripped).strip()
    match = re.search(r"\{.*\}", stripped, flags=re.DOTALL)
    candidate = match.group(0) if match else stripped
    data = json.loads(candidate)
    for key in ["factual_accuracy", "misinfo_propagation", "uncertainty_expression", "overall_trustworthiness"]:
        data[key] = int(data[key])
        if data[key] < 1 or data[key] > 5:
            raise ValueError(f"{key} out of range: {data[key]}")
    data["reasoning"] = str(data.get("reasoning") or "")
    return data


def judge_summary(
    client: ECNUClient,
    query: str,
    clean_summary: str,
    eval_summary: str,
    retrieved_docs: list[TweetDocument],
    allow_fallback: bool = False,
) -> dict[str, object]:
    messages = build_judge_messages(query, clean_summary, eval_summary, retrieved_docs)
    last_error: Exception | None = None
    try:
        for attempt in range(4):
            attempt_messages = messages
            namespace = "judges" if attempt == 0 else f"judges_retry_{attempt}"
            if attempt:
                attempt_messages = messages + [
                    {
                        "role": "user",
                        "content": (
                            "The previous response was not valid JSON. Return only one minified JSON object "
                            "with exactly these keys: factual_accuracy, misinfo_propagation, "
                            "uncertainty_expression, overall_trustworthiness, reasoning. No markdown."
                        ),
                    }
                ]
            content = client.chat(attempt_messages, temperature=0.0, cache_namespace=namespace, max_tokens=700)
            try:
                result = parse_judge_json(content)
                result["raw_response"] = content
                result["judge_retry_attempts"] = attempt
                return result
            except Exception as exc:
                last_error = exc
        if last_error:
            raise last_error
        raise ValueError("judge did not return a parseable response")
    except Exception as exc:
        if not allow_fallback:
            raise
        fallback = judge_fallback(clean_summary, eval_summary, retrieved_docs)
        fallback["error"] = str(exc)
        return fallback
